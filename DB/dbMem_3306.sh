#!/bin/bash

mysql3306PID=$(ps -aef | grep mysqld | grep 3306 | grep -v grep | awk '{print $2}')
echo _____________________________________________________________________________________________________________________________________________________
echo $(date '+%d-%m-%Y %H:%M:%S')
echo
top -b -n1 | grep -w ${mysql3306PID}
echo
ps aux | egrep -w "USER|${mysql3306PID}" | grep -v egrep | awk '{print $2,$3,$4,$5,$6}'
echo
free -g | egrep "total|Mem"
echo _____________________________________________________________________________________________________________________________________________________
